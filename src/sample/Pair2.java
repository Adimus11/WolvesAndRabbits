package sample;

/**
 * Simple class that makes Pair object.
 * @author Micha≈Ç Treter
 */
public class Pair2 {

    /**
     * First element of pair.
     */
    public int x;

    /**
     * Second element of pair.
     */
    public int y;

    /**
     * Basic constructor for Pair object.
     * @param a First element
     * @param b Second element
     */
    public Pair2(int a, int b){
        this.x = a;
        this.y = b;
    }

}
